G_SEX_TYPE_FEMAIL=2;
G_INIT_SEED_FLAG=0;

--随机生成名字
function G_COMMON_CREATE_NAME(nSexType)
LogInfo("CreateName----Start!");
    if G_INIT_SEED_FLAG > 0 then
        math.randomseed(os.time());
        math.random(firstnameCnt);
        G_INIT_SEED_FLAG =1;
    end
    local firstnameCnt=table.getn(G_FIRST_NAME_CFG);
    local sec_com_name_cnt=table.getn(G_COM_SECOND_NAME);
    local sec_male_name_cnt=table.getn(G_MALE_SEC_NAME);
    local sec_female_name_cnt=table.getn(G_FEMALE_SEC_NAME);

    local strWholeName;
    --选择姓
    local nFNameIdx = math.random(firstnameCnt);
    local strFirstName = G_FIRST_NAME_CFG[nFNameIdx][1];
    --选择名字
    local strSecName;
    local nSel=math.random(2);
    if nSel == 2 then
        if nSexType == G_SEX_TYPE_MAIL then
            local nSexNameIdx = math.random(sec_male_name_cnt);
            strSecName = G_MALE_SEC_NAME[nSexNameIdx][1];
        elseif nSexType == G_SEX_TYPE_FEMAIL then
            local nSexNameIdx = math.random(sec_female_name_cnt);
            strSecName = G_FEMALE_SEC_NAME[nSexNameIdx][1];
        end
    else
        --选择通用名
        local nNameIdx = math.random(sec_com_name_cnt);
        strSecName = G_COM_SECOND_NAME[nNameIdx][1];
    end

    strWholeName = strFirstName..strSecName;
    return strWholeName;
end